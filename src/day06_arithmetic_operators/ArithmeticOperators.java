package day06_arithmetic_operators;

public class ArithmeticOperators {
    public static void main (String [] args) {
        System.out.println (5 + 10); // 15
        System.out.println (10 - 3); // 7
        System.out.println (3 * 3); // 9
        System.out.println (15 / 5); // 3
        System.out.println (12 % 5); // 5 + 5 + 5? need to add 2 for summ 12 that mean 2 is REMINDER
        System.out.println (13 % 5);  // 5 + 5 + 3 need to add 3 for summ 13 so that mean 3 is REMINDER

        int chairs = 4 + 2;
        System.out.println (chairs); // printing 6

        int spoons = 10 + 20 + 12 + 0;
        System.out.println (spoons); // = 42 regular additional without any SYMBOLS

        // there are 43 spoons in kitchen
        System.out.println ("There are " + spoons + " spoons in kitchen");






    }
}
