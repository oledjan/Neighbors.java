package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main (String [] args) {
        // ********* WHOLE NUMBERS/INTEGER TYPES **********
        byte byteValue = 100;
        short shortValue = 32766;
        int intValue = 265498;
        long longValue = 55555555555L;
        // *****DECIMAL NUMBERS/FLOATING POINT TYPES ******
        float floatValue = 2345.55F;
        double doubleValue = 3644444.4334; // .4334 D also valid
        // ********CHARACTER TYPE ****************
        char charValue = '1'; // only one character in a single quotes GENDER,
        // CHAR TYPE -> single character, in single quotes '     '
        // in programming
        char ch = 'B'; // used only single qutes '
        // ***** BOOLEAN - is it TRUE or FALSE **********
        boolean booleanValue = false;

        // PRINT VARIABLE VALUES above *****************************
        System.out.println (byteValue);
        System.out.println (shortValue);
        System.out.println (intValue);
        System.out.println (longValue);
        System.out.println (floatValue);
        System.out.println (doubleValue);
        System.out.println (charValue);
        System.out.println (ch);
        System.out.println (booleanValue);






    }
}
